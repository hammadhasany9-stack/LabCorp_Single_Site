export const calculateMetrics = (orders: OrderHistoryItem[]): OrderMetrics => {
  const today = new Date()
  today.setHours(0, 0, 0, 0)
  
  const uniquePlans = new Set(orders.map(order => order.planName))
  const ordersPlacedToday = orders.filter(order => {
    const orderDate = new Date(order.orderDate)
    orderDate.setHours(0, 0, 0, 0)
    return orderDate.getTime() === today.getTime()
  }).length
  
  return {
    totalOrders: orders.length,
    ordersShipped: orders.filter(order => order.status === 'shipped').length,
    ordersCancelled: orders.filter(order => order.status === 'cancelled').length,
    ordersInProgress: orders.filter(order => order.status === 'in_progress').length,
    totalPlans: uniquePlans.size,
    ordersPlacedToday: ordersPlacedToday
  }
}

